        tbPoisson.setModel(poissonTableModel);

        camposVerificar = new Component[]{ tfNumeroTentativas,  tfMedia};
        camposLimpar = new Component[]{ tfNumeroTentativas,tfMedia, tfCodigo, tfResultado};

